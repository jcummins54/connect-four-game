import { checkWinner } from "../js/checkWinner.js";

describe("checkWinner", () => {
  it("Black should win", () => {
    const board = [
      [" ", " ", " ", " ", " ", " ", " "],
      [" ", " ", " ", "☻", " ", " ", " "],
      [" ", " ", "☻", "☺", " ", " ", " "],
      [" ", "☻", "☺", "☻", " ", " ", " "],
      ["☻", "☻", "☻", "☺", "☻", "☺", "☻"],
      ["☺", "☺", "☻", "☻", "☺", "☻", "☺"],
    ];
    const result = checkWinner(board);
    expect(result).toBe("☻");
  });

  it("White should win", () => {
    const board = [
      [" ", " ", " ", "☺", " ", " ", " "],
      [" ", " ", " ", "☻", "☺", " ", " "],
      [" ", " ", "☺", "☺", "☻", "☺", " "],
      [" ", "☻", "☺", "☻", "☻", "☺", "☺"],
      ["☻", "☻", "☻", "☺", "☻", "☺", "☻"],
      ["☺", "☺", "☻", "☻", "☺", "☻", "☺"],
    ];
    const result = checkWinner(board);
    expect(result).toBe("☺");
  });

  it("White should win", () => {
    const board = [
      [" ", "☺", " ", "☻", " ", " ", " "],
      ["☺", "☺", "☻", "☻", "☻", " ", " "],
      ["☺", "☻", "☻", "☺", "☺", " ", " "],
      ["☻", "☺", "☺", "☻", "☻", " ", "☻"],
      ["☺", "☻", "☻", "☻", "☺", "☻", "☺"],
      ["☺", "☻", "☺", "☺", "☻", "☺", "☺"],
    ];
    const result = checkWinner(board);
    expect(result).toBe("☻");
  });

  it("White should win", () => {
    const board = [
      [" ", "☺", " ", " ", " ", " ", " "],
      ["☺", "☺", "☻", "☻", "☻", " ", " "],
      ["☺", "☻", "☻", "☺", "☺", " ", " "],
      ["☻", "☺", "☺", "☻", "☻", "☻", "☻"],
      ["☺", "☻", "☻", "☻", "☺", "☻", "☺"],
      ["☺", "☻", "☺", "☺", "☻", "☺", "☺"],
    ];
    const result = checkWinner(board);
    expect(result).toBe("☻");
  });
});
